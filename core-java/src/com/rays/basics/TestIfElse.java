package com.rays.basics;

public class TestIfElse {

	public static void main(String[] args) {

		int a = 100;

		int b = 1001;

		if (a > b) {

			System.out.println("a is greater");

		} else if (a == b) {

			System.out.println("a equal to b");

		} else {

			System.out.println("b is greater");

		}
	}
}
